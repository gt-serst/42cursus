#include "ft_printf.h"

void	ft_putstr(char *s)
{
	unsigned int	tmp;

	if (!s)
		return ;
	tmp = 0;
	while (s[tmp] != '\0')
	{
		write(1, &s[tmp], 1);
		tmp++;
	}
}
